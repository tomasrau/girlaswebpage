import { writeFileSync } from 'fs';

const endpoint = process.env.SHEET_ENDPOINT;

if (!endpoint) {
  console.warn('SHEET_ENDPOINT not set — generating empty testimonials.ts');
  writeFileSync('src/data/testimonials.ts', emptyFile(), 'utf-8');
  process.exit(0);
}

const res = await fetch(endpoint);
if (!res.ok) throw new Error(`Apps Script fetch failed: ${res.status}`);

const data = await res.json();

if (!Array.isArray(data)) {
  throw new Error(`Unexpected response format: ${JSON.stringify(data).slice(0, 200)}`);
}

writeFileSync('src/data/testimonials.ts', generateFile(data), 'utf-8');
console.log(`✓ Synced ${data.length} approved testimonial(s)`);

function generateFile(testimonials) {
  return `// Auto-generated by sync-testimonials — do not edit manually.
// Source: Google Sheets via Apps Script.
// Last sync: ${new Date().toISOString()}

export interface Testimonial {
  id: string;
  name: string;
  instagramHandle?: string;
  destination: string;
  stars: number;
  nps: number;
  wouldReturn: 'si' | 'talvez' | 'no';
  text: string;
  date: string;
  featured?: boolean;
}

export interface TestimonialMetrics {
  avgStars: number;
  npsScore: number;
  wouldReturnPct: number;
  total: number;
}

export const testimonials: Testimonial[] = ${JSON.stringify(testimonials, null, 2)};

export function getMetrics(data: Testimonial[]): TestimonialMetrics {
  if (!data.length) return { avgStars: 0, npsScore: 0, wouldReturnPct: 0, total: 0 };
  const avgStars = Math.round((data.reduce((s, t) => s + t.stars, 0) / data.length) * 10) / 10;
  const promoters  = data.filter(t => t.nps >= 9).length;
  const detractors = data.filter(t => t.nps <= 6).length;
  const npsScore   = Math.round(((promoters - detractors) / data.length) * 100);
  const returning      = data.filter(t => t.wouldReturn === 'si').length;
  const wouldReturnPct = Math.round((returning / data.length) * 100);
  return { avgStars, npsScore, wouldReturnPct, total: data.length };
}

export function getFeatured(data: Testimonial[], limit = 4): Testimonial[] {
  const featured = data.filter(t => t.featured);
  return (featured.length >= limit ? featured : data).slice(0, limit);
}
`;
}

function emptyFile() {
  return generateFile([]);
}
